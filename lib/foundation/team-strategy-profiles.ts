function clampBias(value: number) {
  return Math.max(1, Math.min(10, Math.round(value)));
}

function levelFromBias(value: number | null | undefined): TeamStrategyLevel {
  const next = typeof value === "number" && Number.isFinite(value) ? value : 5;
  if (next >= 8) return "high";
  if (next <= 3) return "low";
  return "medium";
}

function levelFromComposite(...values: Array<number | null | undefined>): TeamStrategyLevel {
  const valid = values.filter((value): value is number => typeof value === "number" && Number.isFinite(value));
  if (valid.length === 0) {
    return "medium";
  }

  const average = valid.reduce((sum, value) => sum + value, 0) / valid.length;
  return levelFromBias(average);
}

function fallbackTheme(summary: string) {
  const [first] = summary.split(".");
  return first?.trim() || summary.trim();
}

function deriveSecondaryArchetypes(profile: TeamStrategyProfile) {
  if (profile.secondaryArchetypes && profile.secondaryArchetypes.length > 0) {
    return profile.secondaryArchetypes;
  }

  return profile.preferredArchetypes.slice(1, 4);
}

function normalizeStrategyProfile(team: Team, identity: TeamIdentity | null | undefined, profile: TeamStrategyProfile): TeamStrategyProfile {
  const derivedAxisBias = deriveTeamIdentityAxisBias(identity);
  const normalizedBias = {
    ...neutralBias(),
    ...(profile.bias ?? {}),
  };

  return {
    ...profile,
    teamId: team.teamId,
    teamCode: profile.teamCode ?? team.shortCode,
    teamName: profile.teamName ?? team.name,
    strategyVersion: profile.strategyVersion ?? "v1-local",
    fantasyTheme: profile.fantasyTheme ?? fallbackTheme(profile.strategySummary),
    loreTheme: profile.loreTheme ?? profile.strategySummary,
    avoidedArchetypes:
      profile.avoidedArchetypes && profile.avoidedArchetypes.length > 0
        ? profile.avoidedArchetypes
        : profile.dislikedArchetypes ?? [],
    secondaryArchetypes: deriveSecondaryArchetypes(profile),
    dislikedArchetypes: profile.dislikedArchetypes ?? profile.avoidedArchetypes ?? [],
    avoidedRaces:
      profile.avoidedRaces && profile.avoidedRaces.length > 0 ? profile.avoidedRaces : profile.dislikedRaces ?? [],
    dislikedRaces: profile.dislikedRaces ?? profile.avoidedRaces ?? [],
    avoidedClasses:
      profile.avoidedClasses && profile.avoidedClasses.length > 0
        ? profile.avoidedClasses
        : profile.dislikedClasses ?? [],
    dislikedClasses: profile.dislikedClasses ?? profile.avoidedClasses ?? [],
    preferredTraits: profile.preferredTraits ?? [],
    dislikedTraits: profile.dislikedTraits ?? [],
    rosterMinTarget: profile.rosterMinTarget ?? identity?.playerMin ?? null,
    rosterOptTarget: profile.rosterOptTarget ?? identity?.playerOpt ?? null,
    prefersDepth: profile.prefersDepth ?? levelFromBias(normalizedBias.rosterDepthPreference),
    prefersStars: profile.prefersStars ?? levelFromBias(normalizedBias.starPriority),
    prefersAllrounders:
      profile.prefersAllrounders ??
      levelFromComposite(normalizedBias.rosterDepthPreference, 11 - normalizedBias.eliteSmallRosterPreference),
    prefersSpecialists:
      profile.prefersSpecialists ??
      levelFromComposite(normalizedBias.starPriority, normalizedBias.eliteSmallRosterPreference),
    shortContractsBias: profile.shortContractsBias ?? levelFromBias(normalizedBias.shortContractPreference),
    longContractsBias: profile.longContractsBias ?? levelFromBias(normalizedBias.longContractPreference),
    spendAggression: profile.spendAggression ?? levelFromComposite(normalizedBias.starPriority, normalizedBias.riskTolerance),
    saveDiscipline: profile.saveDiscipline ?? levelFromComposite(normalizedBias.cashPriority, normalizedBias.wageSensitivity),
    overpayTolerance: profile.overpayTolerance ?? levelFromComposite(normalizedBias.starPriority, normalizedBias.riskTolerance),
    sellAggression: profile.sellAggression ?? levelFromBias(normalizedBias.sellForProfitAggression),
    profitSellBias: profile.profitSellBias ?? levelFromBias(normalizedBias.sellForProfitAggression),
    loyaltyPreference: profile.loyaltyPreference ?? levelFromBias(normalizedBias.loyaltyBias),
    riskToleranceLevel: profile.riskToleranceLevel ?? levelFromBias(normalizedBias.riskTolerance),
    emergencyBuyBias:
      profile.emergencyBuyBias ??
      levelFromComposite(
        normalizedBias.rosterDepthPreference,
        normalizedBias.riskTolerance,
        profile.rosterMinTarget != null && profile.rosterOptTarget != null && profile.rosterMinTarget < profile.rosterOptTarget ? 7 : 5,
      ),
    powBias: derivedAxisBias?.pow ?? null,
    speBias: derivedAxisBias?.spe ?? null,
    menBias: derivedAxisBias?.men ?? null,
    socBias: derivedAxisBias?.soc ?? null,
    hardNoGos: profile.hardNoGos && profile.hardNoGos.length > 0 ? profile.hardNoGos : profile.lockedNoGos ?? [],
    lineupStyleNote: profile.lineupStyleNote ?? profile.rosterStyle,
    transferStyleNote: profile.transferStyleNote ?? profile.buyStyle,
    sellStyleNote: profile.sellStyleNote ?? profile.sellStyle,
    lockedNoGos: profile.lockedNoGos ?? profile.hardNoGos ?? [],
    strategyWarnings: profile.strategyWarnings ?? [],
    bias: {
      cashPriority: clampBias(normalizedBias.cashPriority),
      valuePriority: clampBias(normalizedBias.valuePriority),
      starPriority: clampBias(normalizedBias.starPriority),
      riskTolerance: clampBias(normalizedBias.riskTolerance),
      wageSensitivity: clampBias(normalizedBias.wageSensitivity),
      sellForProfitAggression: clampBias(normalizedBias.sellForProfitAggression),
      shortContractPreference: clampBias(normalizedBias.shortContractPreference),
      longContractPreference: clampBias(normalizedBias.longContractPreference),
      loyaltyBias: clampBias(normalizedBias.loyaltyBias),
      harmonyStrictness: clampBias(normalizedBias.harmonyStrictness),
      rosterDepthPreference: clampBias(normalizedBias.rosterDepthPreference),
      eliteSmallRosterPreference: clampBias(normalizedBias.eliteSmallRosterPreference),
    },
  };
}

