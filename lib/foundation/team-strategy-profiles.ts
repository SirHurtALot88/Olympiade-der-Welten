export function createDefaultTeamStrategyProfile(team: Team, identity?: TeamIdentity | null): TeamStrategyProfile {
  const seeded = strategySeedsByTeamId[team.teamId];
  const fallbackIdentitySummary = identity
    ? `Numeric team DNA POW ${identity.pow}, SPE ${identity.spe}, MEN ${identity.men}, SOC ${identity.soc}.`
    : "Numeric team DNA available via team identity.";
  const seed = seeded ?? createProfile({ strategySummary: `Default strategy profile for ${team.name}. ${fallbackIdentitySummary}` });

  return normalizeStrategyProfile(team, identity ?? null, {
    teamId: team.teamId,
    ...seed,
  });
}

export function buildTeamStrategyProfileMap(
  teams: Team[],
  identities: TeamIdentity[],
  existing?: Record<string, TeamStrategyProfile> | null,
) {
  return Object.fromEntries(
    teams.map((team) => {
      const current = existing?.[team.teamId];
      const identity = identities.find((entry) => entry.teamId === team.teamId) ?? null;
      const defaults = createDefaultTeamStrategyProfile(team, identity);
      return [
        team.teamId,
        normalizeStrategyProfile(team, identity, {
          ...defaults,
          ...current,
          teamId: team.teamId,
          bias: {
            ...defaults.bias,
            ...(current?.bias ?? {}),
          },
        }),
      ];
    }),
  );
}

export function getTeamStrategyProfile(gameState: GameState, teamId: string) {
  const existing = gameState.seasonState.teamStrategyProfiles?.[teamId];
  if (existing) {
    return existing;
  }

  const team = gameState.teams.find((entry) => entry.teamId === teamId);
  const identity = gameState.teamIdentities.find((entry) => entry.teamId === teamId) ?? null;
  return team ? createDefaultTeamStrategyProfile(team, identity) : null;
}

export function withNormalizedTeamStrategyProfiles(gameState: GameState): GameState {
  return {
    ...gameState,
    seasonState: {
      ...gameState.seasonState,
      teamStrategyProfiles: buildTeamStrategyProfileMap(
        gameState.teams,
        gameState.teamIdentities,
        gameState.seasonState.teamStrategyProfiles,
      ),
    },
  };
}
